// Condicionais

let age = 14;

if(age == 1 || age <= 10) {
    console.log(age)
    console.log("Child")
} else if (age > 10 && age <= 17) {
    console.log(age)
    console.log("Teen")
} else if (age >= 18 && age <= 60) {
    console.log(age)
    console.log("Adult")
} else {
    console.log(age)
    console.log("Elder")
}

